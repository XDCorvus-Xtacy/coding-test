'''
문제 설명
문자열로 구성된 리스트 strings와, 정수 n이 주어졌을 때, 
각 문자열의 인덱스 n번째 글자를 기준으로 오름차순 정렬하려 합니다. 
예를 들어 strings가 ["sun", "bed", "car"]이고 n이 1이면 
각 단어의 인덱스 1의 문자 "u", "e", "a"로 strings를 정렬합니다.

제한 조건
strings는 길이 1 이상, 50이하인 배열입니다.
strings의 원소는 소문자 알파벳으로 이루어져 있습니다.
strings의 원소는 길이 1 이상, 100이하인 문자열입니다.
모든 strings의 원소의 길이는 n보다 큽니다.
인덱스 1의 문자가 같은 문자열이 여럿 일 경우, 사전순으로 앞선 문자열이 앞쪽에 위치합니다.
'''
def solution(strings, n):
    return sorted(strings, key=lambda x: (x[n], x))


'''
정렬 key로 튜플 반환하기 (다단계 정렬)

원리: 튜플은 앞 요소부터 비교, 같으면 다음 요소로 (사전식 비교)
      → key 튜플에 정렬 기준을 우선순위 순서로 나열

기본:   key=lambda x: (1차, 2차, 3차, ...)
내림섞기: key=lambda x: (-x.score, x.name)   # 숫자는 - 로 내림, 문자열은 그대로 오름
튜플꺼내기: key=lambda x: (-x[1], x[0])

주의:
- reverse=True는 "전체" 기준을 뒤집음 (부분 내림 불가)
- 마이너스(-) 부호는 숫자에만 가능 (문자열 X)
- 튜플은 lambda 문법이 아니라 "key가 반환하는 값"일 뿐
'''