'''
문제 설명
연속된 세 개의 정수를 더해 12가 되는 경우는 3, 4, 5입니다. 
두 정수 num과 total이 주어집니다. 연속된 수 num개를 더한 값이 
total이 될 때, 정수 배열을 오름차순으로 담아 return하도록 
solution함수를 완성해보세요.

제한사항
1 ≤ num ≤ 100
0 ≤ total ≤ 1000
num개의 연속된 수를 더하여 total이 될 수 없는 테스트 케이스는 없습니다.
'''
def solution(num, total):
    start = (total - num * (num - 1) // 2) // num
    return [start + i for i in range(num)]

'''
아이디어
예를 들어 연속된 세 수를 더한 값은
n, n+1, n+2
3n + 3 = 3(n+1)
죽, 중간값의 3배이다.
연속된 숫자의 합은 공차가 1인 등차수열이므로
전체 수의 평균에 더하는 숫자 양만큼이다.
3 3 3 3 3
'''

