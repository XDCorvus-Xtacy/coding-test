'''
문제 설명
머쓱이는 큰 종이를 1 x 1 크기로 자르려고 합니다. 예를 들어 2 x 2 
크기의 종이를 1 x 1 크기로 자르려면 최소 가위질 세 번이 필요합니다.
정수 M, N이 매개변수로 주어질 때, M x N 크기의 종이를 최소로 가위질 
해야하는 횟수를 return 하도록 solution 함수를 완성해보세요.
'''
def solution(M, N):
    return M*N-1

'''
아이디어
일단 하나를 기준으로 잡음. 그 기준 축을 M이라고 하자.
그럼 기준 축 방향으로는 M-1만큼만 자를 수 있음.
그러면 종이는 m개만큼 생김.
그리고 하나의 조각은 다시 나머지 길이인 N-1만큼 자를 수 있음.
이걸 m번 반복하므로 결국 조각은
M-1 + M(N-1)
= M + M(N-1) -1
= M(N-1 + 1) -1
= M*N-1
결국 결과값은 M*N-1로 도출됨.
간단하게 직관적으로 생각하면 가위질 한번 하면 종이가 1씩 늘어남.
그러므로 M*N 크기의 종이를 1*1로 나누려면 M*N-1만큼 잘라야함.
'''

