'''
문제 설명
등차수열 혹은 등비수열 common이 매개변수로 주어질 때, 마지막 원소 
다음으로 올 숫자를 return 하도록 solution 함수를 완성해보세요.

제한사항
2 < common의 길이 < 1,000
-1,000 < common의 원소 < 2,000
common의 원소는 모두 정수입니다.
등차수열 혹은 등비수열이 아닌 경우는 없습니다.
등비수열인 경우 공비는 0이 아닌 정수입니다.
'''
def solution(common):
    com = common
    if (com[0]+com[-1])*len(com) == 2*sum(com):
        return com[-1] + (com[1]-com[0])
    else:
        return com[-1]*com[1]//com[0]

'''
아이디어
이 수열은 등차수열이 아니면 등비수열이다.
배반사건이므로 등차수열인지 아닌지 검증을 통해서
등차수열이면 등차수열 풀이로, 등비수열이면 등비수열
풀이로 풀면 된다.
만일 등차수열일 경우,
즉,
(common[0]+common[-1])*len(common) = 2*sum(common)
위 식이 성립할 경우가 등차수열이다.
이를 바탕으로 답을 구하면 된다.
'''

# 정석풀이
def solution(common):
    # 등차 판별: 앞 두 항의 차이 == 그 다음 두 항의 차이?
    if common[1] - common[0] == common[2] - common[1]:
        # 등차수열: 공차를 더함
        d = common[1] - common[0]
        return common[-1] + d
    else:
        # 등비수열: 공비를 곱함
        r = common[1] // common[0]
        return common[-1] * r

