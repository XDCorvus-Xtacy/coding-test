'''
# 문제 설명
# 정수 num1과 num2가 주어질 때, num1과 num2의 합을 
return하도록 soltuion 함수를 완성해주세요.

# 제한사항
# -50,000 ≤ num1 ≤ 50,000
# -50,000 ≤ num2 ≤ 50,000
'''
def solution(num1, num2):
    return num1 + num2

###################################################
'''
# 문제 설명
# 정수 num1과 num2가 주어질 때, num1에서 num2를 뺀 
값을 return하도록 soltuion 함수를 완성해주세요.

# 제한사항
# -50000 ≤ num1 ≤ 50000
# -50000 ≤ num2 ≤ 50000
'''
def solution(num1, num2):
    return num1 - num2

###################################################
'''
# 문제 설명
# 정수 num1, num2가 매개변수 주어집니다. num1과 num2를 
곱한 값을 return 하도록 solution 함수를 완성해주세요.

# 제한사항
# 0 ≤ num1 ≤ 100
# 0 ≤ num2 ≤ 100
'''
def solution(num1, num2):
    return num1 * num2

###################################################
'''
# 정수 num1, num2가 매개변수로 주어질 때, num1을 num2로 
나눈 몫을 return 하도록 solution 함수를 완성해주세요.

# 제한사항
# 0 < num1 ≤ 100
# 0 < num2 ≤ 100
'''
def solution(num1, num2):
    return num1 // num2